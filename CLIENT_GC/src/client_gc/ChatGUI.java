    public void getMessage() throws IOException {
        int hide = 0;
        String chatServer;
        BufferedReader chatFromServer = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()));
        chatServer = chatFromServer.readLine();
        for (String id : listBlocked) {
            if (chatServer.substring(chatServer.indexOf("#") + 1, chatServer.indexOf(":")).equals(id)) {
                hide = 1;
            }
        }
        if (hide == 0) {
            txtChat.append(chatServer + "\n");
        }
    }

    public void getUser(String[] u) {
        user = u;
        nameLbl.setText("Chatting as: " + user[1]);
        listBlocked = checkBlokir(user[0]);
    }

