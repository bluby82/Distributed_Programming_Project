            DataOutputStream sendToServer
                    = new DataOutputStream(clientSocket.getOutputStream());

            //method untuk send chat ke lawan (writeBytes)
            sendToServer.writeBytes(user[1] + "#" + user[0] + ": " + txtPesan.getText() + "\n");
            txtPesan.setText("");
            txtPesan.requestFocus();
