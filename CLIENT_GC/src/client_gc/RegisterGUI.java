    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        try {
            if (!passwordTxt.getText().equals(passwordTxt2.getText())) {
                JOptionPane.showMessageDialog(this, "Password yang diinputkan tidak sesuai");
            } else if ((passwordTxt.getText().equals(passwordTxt2.getText()))) {
                String email = emailTxt.getText();
                String pass = passwordTxt.getText();
                
                email = toHexString(getSHA(email));
                pass = toHexString(getSHA(pass));
                System.out.println(email);
                System.out.println(pass);
                String result = register(usernameTxt.getText(), email, pass);
                if (result.equals("emailDuplikat")) {
                    JOptionPane.showMessageDialog(this, "Email sudah digunakan akun lain");
                }
                else{
                    JOptionPane.showMessageDialog(this, "Registrasi sukses, silahkan log in");
                    this.setVisible(false);
                    new LoginGUI().setVisible(true);
                }
            }
        } catch (Exception e) {
            System.out.println("Error di btnCreate: " + e);
        }
    }//GEN-LAST:event_createBtnActionPerformed

