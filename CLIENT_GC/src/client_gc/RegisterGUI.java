    private static String register(java.lang.String username, java.lang.String email, java.lang.String password) {
        com.project.service.ProjectWebService_Service service = new com.project.service.ProjectWebService_Service();
        com.project.service.ProjectWebService port = service.getProjectWebServicePort();
        return port.register(username, email, password);
    }

    private static byte[] getSHA(java.lang.String input) throws NoSuchAlgorithmException_Exception {
        com.project.service.ProjectWebService_Service service = new com.project.service.ProjectWebService_Service();
        com.project.service.ProjectWebService port = service.getProjectWebServicePort();
        return port.getSHA(input);
    }

    private static String toHexString(byte[] hash) {
        com.project.service.ProjectWebService_Service service = new com.project.service.ProjectWebService_Service();
        com.project.service.ProjectWebService port = service.getProjectWebServicePort();
        return port.toHexString(hash);
    }

